import type {
  CategoryId,
  Invention,
  InventionImageType,
} from "@/types";

type InventionSeed = Omit<
  Invention,
  "imageSrc" | "imageAlt" | "imageType" | "imageSourceUrl" | "imageCredit"
> & Partial<Pick<Invention, "imageAlt" | "imageType" | "imageSourceUrl" | "imageCredit">>;

function imagePath(id: string) {
  return `/api/invention-image/${id}`;
}

function createInvention(seed: InventionSeed): Invention {
  const resolvedImageType: InventionImageType = seed.imageType ?? (seed.patentNumber
    ? "patent"
    : seed.year < 1950
      ? "archival"
      : "photo");
  const resolvedAlt = resolvedImageType === "patent"
    ? `Patent or archival image of ${seed.title}`
    : `Historical image of ${seed.title}`;

  return {
    ...seed,
    imageSrc: imagePath(seed.id),
    imageAlt: resolvedAlt,
    imageType: resolvedImageType,
    imageSourceUrl: seed.imageSourceUrl ?? seed.sourceUrl,
    imageCredit: seed.imageCredit ?? "Wikimedia Commons",
  };
}

